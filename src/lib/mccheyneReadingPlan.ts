// Helper functions for the app
export const getReadingByDay = (day: number): McCheyneReading | undefined => {
  return mcCheyneReadingPlan.find(reading => reading.day === day);
};

export const getCurrentDayNumber = (): number => {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  return Math.floor(diff / oneDay);
};

export const calculateReadingProgress = (checkedChapters: string[], allChapters: string[]): number => {
  if (allChapters.length === 0) return 0;
  return Math.round((checkedChapters.length / allChapters.length) * 100);
};

export const getCurrentDayReading = (): McCheyneReading | undefined => {
  return getReadingByDay(getCurrentDayNumber());
};
